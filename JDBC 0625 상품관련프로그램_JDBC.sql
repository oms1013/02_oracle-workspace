CREATE TABLE PRODUCT(
    
    PRODUCT_ID VARCHAR2(50) PRIMARY KEY,
    P_NAME VARCHAR2(20) NOT NULL,
    PRICE NUMBER NOT NULL,
    DESCRIPTION VARCHAR(30),
    STOCK NUMBER NOT NULL
);

